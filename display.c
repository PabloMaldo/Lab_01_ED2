//******************************************************************************
//  Funcion para tomar los bits mas significativos
//******************************************************************************
char upper_bits(char number){
    char up = (number >> 4) & 0x0F;
    
    return up;     
}
//******************************************************************************
//******************************************************************************

//******************************************************************************
//  Funcion para tomar los bits mas significativos
//******************************************************************************
char lower_bits(char number){
    char low = number & 0x0F;
    
    return low;     
}
